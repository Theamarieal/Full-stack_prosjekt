package ntnu.no.fs_v26.model;

/**
 * Enumeration representing the type of training document.
 *
 * <p>This enum is used to classify training documents based on their
 * purpose and content. It distinguishes between formal policy documents
 * and general training material.</p>
 */
public enum TrainingDocumentType {

    /**
     * A policy document containing rules, requirements, or formal guidelines.
     */
    POLICY,

    /**
     * A training document intended for instruction, learning, or assessment.
     */
    TRAINING
}